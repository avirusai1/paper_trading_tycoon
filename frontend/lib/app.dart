/// Paper Trading Tycoon — Root Application Widget
///
/// Attaches the GoRouter, MaterialApp theme, and Riverpod consumer.
/// All navigation, theming, and localization configuration lives here.
library;

import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';

import 'core/theme/app_theme.dart';
import 'routes/app_router.dart';

class PaperTradingTycoonApp extends ConsumerWidget {
  const PaperTradingTycoonApp({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final router = ref.watch(appRouterProvider);
    final themeMode = ref.watch(themeModeProvider);

    return MaterialApp.router(
      title: 'Paper Trading Tycoon',
      debugShowCheckedModeBanner: false,
      theme: AppTheme.lightTheme,
      darkTheme: AppTheme.darkTheme,
      themeMode: themeMode,
      routerConfig: router,
    );
  }
}
